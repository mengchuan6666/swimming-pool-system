package com.stylefeng.guns.modular.biz.api.model;

public class UserVO {
    public String username;
    public String password;
    public String type;
    public boolean remeber;

    public String email;
    public String nickname;
    public String phone;
    public String passtwo;




    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public boolean isRemeber() {
        return remeber;
    }

    public void setRemeber(boolean remeber) {
        this.remeber = remeber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPasstwo() {
        return passtwo;
    }

    public void setPasstwo(String passtwo) {
        this.passtwo = passtwo;
    }
}
